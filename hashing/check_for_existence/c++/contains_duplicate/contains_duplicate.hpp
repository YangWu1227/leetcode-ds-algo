// contains_duplicate.hpp
#ifndef CONTAINS_DUPLICATE_HPP
#define CONTAINS_DUPLICATE_HPP
#include <vector>

/**
 * @brief Given an integer array `nums`, return `true` if any value appears at least twice in the array, and return `false` if every element is distinct.
 * 
 * @param nums A vector of positive integers
 * @return bool true of there is a value that appears twice, false otherwise 
 */
bool containsDuplicate(const std::vector<int> &nums);

#endif
