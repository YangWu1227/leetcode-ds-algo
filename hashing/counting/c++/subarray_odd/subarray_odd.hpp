// subarray_odd.hpp
#ifndef SUBARRAY_ODD_HPP
#define SUBARRAY_ODD_HPP
#include <vector>

/**
 * @brief Given an array of positive integers `nums` and an integer `k`, find the number of subarrays with exactly `k` odd numbers.
 *
 * @param nums A vector of integers
 * @param k The number of odd numbers to find in the subarrays
 * @return int The number of subarrays with exactly `k` odd numbers
 */
int subarrayOdd(const std::vector<int> &nums, int k);

#endif
