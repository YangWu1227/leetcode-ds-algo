#ifndef MAX_FREQ_HPP
#define MAX_FREQ_HPP
#include <vector>

/**
 * @brief Given a vector `nums` consisting of positive integers, return the total count of the elements in `nums` that appear with the highest frequency.
 *
 * @param nums A vector of positive integers
 * @return int Total count of all elements that appear with the highest frequency
 */
int maxFreq(const std::vector<int> &nums);

#endif
