// largest_unique_number.hpp
#ifndef LARGEST_UNIQUE_NUMBER_HPP
#define LARGEST_UNIQUE_NUMBER_HPP
#include <vector>

/**
 * @brief Given an integer array `nums`, return the largest integer that only occurs once. If no integer occurs once, return `-1`.
 *
 * @param nums A vector a positive integer
 * @return int Largest integer that appeared once or -1 if no such integer exist
 */
int largestUniqueNumber(const std::vector<int> &nums);

#endif
