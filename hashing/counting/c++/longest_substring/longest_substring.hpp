// longest_substring.hpp
#ifndef LONGEST_SUBSTRING_HPP
#define LONGEST_SUBSTRING_HPP
#include <string>

/**
 * @brief Given a string `s` and an integer `k`. Find the length of the longest substring that contains at most `k` distinct characters.
 *
 * @param s A string of lowercase English letters
 * @param k  An integer
 * @return int The length of the longest substring that contains at most `k` distinct characters
 */
int longestSubstring(const std::string &s, const int k);

#endif
