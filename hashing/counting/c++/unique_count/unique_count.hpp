#ifndef UNIQUE_COUNT_HPP
#define UNIQUE_COUNT_HPP
#include <vector>

/**
 * @brief Given an array of integers, return true if all elements have unique counts.
 *
 * @param nums A sequence of integers, which could be negative or positive
 * @return bool `true` if all elements have unique counts otherwise `false`
 */
bool uniqueCount(const std::vector<int> &nums);

#endif
