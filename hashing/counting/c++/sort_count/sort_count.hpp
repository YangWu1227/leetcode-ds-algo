// sort_count.hpp
#ifndef SORT_COUNT_HPP
#define SORT_COUNT_HPP
#include <string>

/**
 * @brief Given a string `s`, sort it in decreasing order based on the count of its characters. The count of a character is the number of times it appears in the string.
 *
 * @param s A string consisting of lower and upper case english letters as well as digits
 * @return std::string Sorted string in decreasing order based on the count of its characters
 */
std::string sortCount(const std::string &s);

#endif
