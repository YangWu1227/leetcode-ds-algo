// max_vowels.h
#ifndef MAX_VOWELS_H
#define MAX_VOWELS_H
#include <string>

/**
 * @brief Given a string `s` and an integer `k`, return the maximum number of vowel letters in any substring of `s` with length `k`.
 *
 * @param s A string
 * @param k An integer
 * @return int The maximum number of vowel letters in any substring of `s` with length `k`
 *
 * @note Vowel letters in English are 'a', 'e', 'i', 'o', and 'u'.
 */
int maxVowels(const std::string &s, int k);

#endif
