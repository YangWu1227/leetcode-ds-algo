// is_subsequence.h
#ifndef IS_SUBSEQUENCE_H
#define IS_SUBSEQUENCE_H

#include <string>

/**
 * @brief Given two strings `s` and `t`, return True if `s` is a subsequence of `t`, or False otherwise.
 *
 * @param s String for which to check if it is a subsequence of `t`.
 * @param t String of which to check if `s` is a subsequence.
 * @return bool true if `s` is a subsequence of `t`, false if not.
 */
bool isSubsequence(const std::string &s, const std::string &t);

#endif
