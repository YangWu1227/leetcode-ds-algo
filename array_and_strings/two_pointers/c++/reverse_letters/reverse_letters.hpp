// reverse_letters.hpp
#ifndef REVERSE_LETTERS_HPP
#define REVERSE_LETTERS_HPP
#include <string>

/**
 * @brief Given a string s, reverse the string based on the rules specified below.
 *
 * @param s The string to reverse
 * @return The string with the letters reversed
 *
 * @note 1. All the characters that are not English letters remain in the same position.
 * @note 2. All the English letters (lowercase or uppercase) should be reversed.
 */
std::string reverseLetters(std::string &s);

#endif
